package com.cdd.bean;

public class UpBook {

	private int id;
	private String book_name;
	private String book_money;
	private String book_picture;
	private String book_page;
	private String book_uptime;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getBook_name() {
		return book_name;
	}
	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	public String getBook_money() {
		return book_money;
	}
	public void setBook_money(String book_money) {
		this.book_money = book_money;
	}
	public String getBook_picture() {
		return book_picture;
	}
	public void setBook_picture(String book_picture) {
		this.book_picture = book_picture;
	}
	public String getBook_page() {
		return book_page;
	}
	public void setBook_page(String book_page) {
		this.book_page = book_page;
	}
	public String getBook_uptime() {
		return book_uptime;
	}
	public void setBook_uptime(String book_uptime) {
		this.book_uptime = book_uptime;
	}
	
	
	
	
}
