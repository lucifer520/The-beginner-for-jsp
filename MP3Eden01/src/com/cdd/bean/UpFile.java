package com.cdd.bean;

public class UpFile {

	private int id;
	private String file_name;
	private String file_uploadPerson;
	private String file_uptime;
	private String file_downPerson;
	private String file_address;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFile_name() {
		return file_name;
	}
	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}
	public String getFile_uploadPerson() {
		return file_uploadPerson;
	}
	public void setFile_uploadPerson(String file_uploadPerson) {
		this.file_uploadPerson = file_uploadPerson;
	}
	public String getFile_uptime() {
		return file_uptime;
	}
	public void setFile_uptime(String file_uptime) {
		this.file_uptime = file_uptime;
	}
	public String getFile_downPerson() {
		return file_downPerson;
	}
	public void setFile_downPerson(String file_downPerson) {
		this.file_downPerson = file_downPerson;
	}
	public String getFile_address() {
		return file_address;
	}
	public void setFile_address(String file_address) {
		this.file_address = file_address;
	}
	
	
	
	
}
